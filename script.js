/* -----------------------------
           NEUSTER BERICHT FÜR STARTSEITE
        ----------------------------- */
function loadLatestReport() {
  reports.sort((a, b) => new Date(b.date) - new Date(a.date));
  document.getElementById("reportContent").innerHTML = reports[0].load();
}

/* -----------------------------
           CARDS (BERICHTE-ÜBERSICHT)
        ----------------------------- */
function loadReportCards() {
  let html = "";

  reports.forEach((r) => {
    html += `
                    <div class="bg-white shadow-lg rounded-xl overflow-hidden cursor-pointer hover:shadow-xl transition"
                         onclick="openReport(${reports.indexOf(r)})">

                        <img src="${r.image}" class="w-full h-48 object-cover">
                        
                        <div class="p-5 text-center">
                            <h3 class="text-xl font-bold text-[#2f8b84]">${r.title}</h3>
                        </div>

                    </div>
                `;
  });

  document.getElementById("reportsGrid").innerHTML = html;
}

/* -----------------------------
           BERICHT ÖFFNEN (Cards verschwinden)
        ----------------------------- */
function openReport(index) {
  const report = reports[index];

  document.getElementById("berichteView").classList.add("hidden");
  document.getElementById("singleReportView").classList.remove("hidden");

  document.getElementById("singleReportContent").innerHTML = report.load();
}

/* -----------------------------
           ZURÜCK ZU CARDS
        ----------------------------- */
function goBackToCards() {
  document.getElementById("singleReportView").classList.add("hidden");
  document.getElementById("berichteView").classList.remove("hidden");
}

window.onload = () => {
  loadLatestReport();
  loadReportCards();
};

/* NAVIGATION */
function showSection(id) {
  document
    .querySelectorAll("section")
    .forEach((sec) => sec.classList.add("hidden"));
  document.getElementById(id).classList.remove("hidden");
  window.scrollTo({ top: 0, behavior: "smooth" });
}

function toggleMobileMenu() {
  document.getElementById("mobileMenu").classList.toggle("hidden");
}
